num1 = int(input('Enter a number: '))
num2 = int(input('Enter a number: '))
sum = float(num1) + float(num2) #add two numbers
print('Sum of {0} and {1} is {2}'.format(num1,num2,sum))
sub = num1-num2 #subtract two numbers
print('Subtraction of {0} and {1} is {2}'.format(num1,num2,sub))
mul = num1*num2 #multiply two numbers
print('Muliplication of {0} and {1} is {2}'.format(num1,num2,mul))
div = num1/num2 #division of two numbers
print('Division of {0} and {1} is {2}'.format(num1,num2,div))
